#include<stdio.h>
int main()
{
    int n,s,d,y=0,sd,t,r=0;
    scanf("%d",&n);//12
    s=n*n;//144
    t=n;//12
    while(n)
    {
        d=n%10;
        r=r*10+d;//21
        n=n/10;
    }
    sd=r*r;//441
    while(sd)
    {
        t=sd%10;
        y=y*10+t;//144
        sd=sd/10;
    }
    if(s==y)
    {
        printf("True");
    }
    else
    {
        printf("False");
    }
}